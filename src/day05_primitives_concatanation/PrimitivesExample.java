package day05_primitives_concatanation;

public class PrimitivesExample {
    public static void main(String[] args){
        byte byteValue=100;
        short shortValue=-32000;
        int intValue=212325345;
        long longValue= 5223452323342L; //long will always have an L at the end
        //***** DECIMAL NUMBER/FLOATING POINT TYPES ****
        float floatValue=2345.55F; //We always put F at the end
        double doubleValue=2355432.4334D; //D is optional on this
        //***** CHARACTER TYPE *****
        char charValue='a'; //char is a single character in single quotes
        //char lastLetterOfMyLastName='E';
        //***** Boolean= True|False *****
        boolean booleanValue=true;
        System.out.println(byteValue);
        System.out.println(shortValue);
        System.out.println(intValue);
        System.out.println(longValue);
        System.out.println(floatValue);
        System.out.println(doubleValue);
        System.out.println(charValue);
        System.out.println(booleanValue);

        //PRINT VAIRABLE VALUES




    }

}
